def get_articles_by_category(category):
    return [a for a in ARTICLES.values() if a["category"] == category]

def get_all_categories():
    return list(set(a["category"] for a in ARTICLES.values()))

def get_related(current_slug, limit=3):
    current = ARTICLES.get(current_slug)
    if not current: return []
    related = [a for a in ARTICLES.values() if a["category"] == current["category"] and a["slug"] != current_slug]
    return related[:limit]
